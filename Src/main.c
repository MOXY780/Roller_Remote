/**
  * @brief  This function build the TX Buffer for UART communication
  * @param  Struct of Remote
  * @param  Ptr to TX Buffer
	* @retval State of Function: HAL_OK, HAL_ERROR
  */
HAL_StatusTypeDef Build_TX(TRemote *remote, uint8_t *TX_buffer)
{
	switch (remote->Joystick)
	{
		case ST_HALT:
			TX_buffer[0] = HALT;
			break;
		case ST_FORW:
			TX_buffer[0] = FORW;
			break;
		case ST_FORWRIGHT:
			TX_buffer[0] = FORWR;
			break;
		case ST_RIGHT:
			TX_buffer[0] = RIGHT;
			break;
		case ST_BACKRIGHT:
			TX_buffer[0] = BACKR;
			break;
		case ST_BACK:
			TX_buffer[0] = BACK;
			break;
		case ST_BACKLEFT:
			TX_buffer[0] = BACKL;
			break;
		case ST_LEFT:
			TX_buffer[0] = LEFT;
			break;
		case ST_FORWLEFT:
			TX_buffer[0] = FORWL;
			break;
		default:
			TX_buffer[0] = HALT;
			return HAL_ERROR;
	}
	
	if(remote->Drivemode == B_VIBR)
		TX_buffer[0] |= VIBR;
	else 
		TX_buffer[0] &= NORMAL;
	
	if(remote->Drivespeed == B_FAST)
		TX_buffer[0] |= FAST;
	else 
		TX_buffer[0] &= SLOW;
	
	if(remote->Ign == B_ON)
		TX_buffer[0] |= ON;
	else
		TX_buffer[0] &= OFF;
	
	return HAL_OK;
}

